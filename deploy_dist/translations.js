// Current language (default to English)
let currentLanguage = localStorage.getItem('selected_language') || 'en';

// Function to get translation for a key
function t(key) {
    const lang = TRANSLATIONS[currentLanguage];
    if (lang && lang[key]) {
        return lang[key];
    }
    // Fallback to English if translation not found
    const english = TRANSLATIONS['en'];
    return english[key] || key;
}

// Function to switch language
function switchLanguage(langCode) {
    if (TRANSLATIONS[langCode]) {
        currentLanguage = langCode;
        localStorage.setItem('selected_language', langCode);
        
        // Apply translations to the page
        applyTranslations();
        
        // Update language toggle button text
        updateLanguageToggle();
        
        return true;
    }
    return false;
}

// Function to apply translations to the page
function applyTranslations() {
    // Translate elements with data-translate attribute
    const translatableElements = document.querySelectorAll('[data-translate]');
    translatableElements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (key) {
            element.textContent = t(key);
        }
    });
    
    // Translate placeholder attributes
    const translatablePlaceholders = document.querySelectorAll('[data-translate-placeholder]');
    translatablePlaceholders.forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (key) {
            element.placeholder = t(key);
        }
    });
    
    // Translate title attributes
    const translatableTitles = document.querySelectorAll('[data-translate-title]');
    translatableTitles.forEach(element => {
        const key = element.getAttribute('data-translate-title');
        if (key) {
            element.title = t(key);
        }
    });
    
    // Translate alt attributes
    const translatableAlts = document.querySelectorAll('[data-translate-alt]');
    translatableAlts.forEach(element => {
        const key = element.getAttribute('data-translate-alt');
        if (key) {
            element.alt = t(key);
        }
    });
}

// Function to update language toggle button
function updateLanguageToggle() {
    const langToggle = document.querySelector('.language-toggle');
    if (langToggle) {
        let langText = 'English';
        if (currentLanguage === 'hi') langText = 'हिंदी';
        if (currentLanguage === 'mr') langText = 'मराठी';
        langToggle.innerHTML = `<span class="material-symbols-outlined text-primary" style="width: 20px; height: 20px;">translate</span><span class="text-sm font-bold text-text-main dark:text-white">${langText}</span>`;
    }
}

// Function to initialize language selector dropdown
function initLanguageSelector() {
    // Create language selector if it doesn't exist
    const existingToggle = document.querySelector('.language-toggle');
    if (!existingToggle) {
        // Look for a place to insert the language toggle (typically in header)
        const headerRight = document.querySelector('.absolute.top-0.right-0') || 
                           document.querySelector('.header .flex.items-center.gap-6:last-child') ||
                           document.querySelector('header .flex.items-center.gap-6:last-child');
        
        if (headerRight) {
            const langToggle = document.createElement('div');
            langToggle.className = 'language-toggle absolute top-0 right-0 p-4 sm:p-6 z-10';
            langToggle.innerHTML = `<button class="flex items-center gap-2 bg-white dark:bg-zinc-800 border border-[#d1d1c8] dark:border-zinc-700 px-3 py-2 rounded-sm shadow-sm hover:bg-gray-50 transition-colors">
                <span class="material-symbols-outlined text-primary" style="width: 20px; height: 20px;">translate</span>
                <span class="text-sm font-bold text-text-main dark:text-white">${currentLanguage === 'hi' ? 'हिंदी' : currentLanguage === 'mr' ? 'मराठी' : 'English'}</span>
            </button>`;
            
            // Add click handler to show language options
            langToggle.querySelector('button').addEventListener('click', function(e) {
                e.stopPropagation();
                
                // Remove existing language menu if present
                const existingMenu = document.querySelector('.language-menu');
                if (existingMenu) existingMenu.remove();
                
                // Create language menu
                const menu = document.createElement('div');
                menu.className = 'language-menu absolute right-4 top-16 bg-white dark:bg-zinc-800 border border-[#d1d1c8] dark:border-zinc-700 rounded-sm shadow-lg z-20 w-40';
                menu.innerHTML = `
                    <div class="py-2">
                        <div class="px-4 py-2 hover:bg-gray-100 dark:hover:bg-zinc-700 cursor-pointer lang-option" data-lang="en">English</div>
                        <div class="px-4 py-2 hover:bg-gray-100 dark:hover:bg-zinc-700 cursor-pointer lang-option" data-lang="hi">हिंदी</div>
                        <div class="px-4 py-2 hover:bg-gray-100 dark:hover:bg-zinc-700 cursor-pointer lang-option" data-lang="mr">मराठी</div>
                    </div>
                `;
                
                // Add click handlers to language options
                menu.querySelectorAll('.lang-option').forEach(option => {
                    option.addEventListener('click', function() {
                        const lang = this.getAttribute('data-lang');
                        switchLanguage(lang);
                        menu.remove();
                    });
                });
                
                document.body.appendChild(menu);
                
                // Close menu when clicking elsewhere
                const closeMenu = function() {
                    menu.remove();
                    document.removeEventListener('click', closeMenu);
                };
                setTimeout(() => document.addEventListener('click', closeMenu), 100);
            });
            
            headerRight.appendChild(langToggle);
        }
    } else {
        // Update existing toggle
        updateLanguageToggle();
    }
}

// Initialize language system when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    // Apply initial translations
    applyTranslations();
    
    // Initialize language selector
    initLanguageSelector();
});

